TOKEN_BOUNDARY_RX = r"(?<![\u0900-\u097F]){tok}(?![\u0900-\u097F])"


def apply_final_cleanup(text: str) -> str:
    """Final conservative cleanup pass after the generic OCR repair.

    - TOKEN_FIXES are applied with explicit Devanagari token boundaries.
    - PHRASE_FIXES are substring replacements for high-frequency forms where
      the corruption always appears as a whole morpheme/stem.
    """
    if not text:
        return text
    for bad, good in TOKEN_FIXES.items():
        text = re.sub(TOKEN_BOUNDARY_RX.format(tok=re.escape(bad)), good, text)
    for bad in sorted(PHRASE_FIXES, key=len, reverse=True):
        if bad in text:
            text = text.replace(bad, PHRASE_FIXES[bad])
    return text


def repair(text: str) -> str:
    """Full OCR-repair pipeline: stray-marks pass, then the rule-based
    character pass, then word-level overrides. Run word-fixes BOTH before
    and after the char pass so that anchored mappings (e.g. exact corrupted
    forms) take precedence over the heuristics, while heuristics catch
    anything the dictionary missed."""
    if not text:
        return text
    text = fix_stray_marks(text)
    # Pre-pass dictionary so anchored fixes beat the heuristic char pass
    text = apply_word_fixes(text)
    text = char_pass(text)
    # Post-pass dictionary in case char pass produced new dictionary keys
    text = apply_word_fixes(text)
    text = apply_final_cleanup(text)
    return text


def clean_pdf_to_lines() -> List[str]:
    if not SOURCE_PDF.exists():
        raise SystemExit(f"Source PDF not found: {SOURCE_PDF}")
    doc = fitz.open(SOURCE_PDF)
    lines: List[str] = []
    for page in doc:
        text = page.get_text()
        for raw in text.split("\n"):
            if is_noise(raw):
                continue
            line = raw.strip()
            if not line:
                continue
            lines.append(line)
    doc.close()
    return lines


