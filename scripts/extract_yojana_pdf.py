# Stats / facts section
# --------------------------------------------------------------------------- #
def parse_stats(lines: List[str]) -> List[str]:
    """Pick up only the body version of the 'Important Yojana Statistics
    (December 2023)' section. The body header is 'योजनासांबांधी
    महत्त्वाची आकडेिारी (डडसेंबर २०२३ पयंत)' (around line 1572). The
    section ends at the first PYQ (Q1)."""
    start = -1
    for i in range(BODY_START_LINE, len(lines)):
        ln = lines[i]
        if (
            ("योजनासांबांधी" in ln or "योजनासांबंधी" in ln)
            and ("आकडेिारी" in ln or "आकडेवारी" in ln)
        ):
            start = i
            break
    if start < 0:
        return []
    end = next(
        (i for i, ln in enumerate(lines[start + 1:], start + 1)
         if ln.strip().startswith("Q1)")),
        len(lines),
    )
    chunk = lines[start + 1: end]
    flowed = [s for s in join_continuations(chunk) if s.strip()]
    cleaned = [repair(s) for s in flowed]
    # Drop tiny garbage lines (single chars left over from OCR layout)
    return [s for s in cleaned if len(s) >= 12 and DEVANAGARI.search(s)]


def run_final_repair_pass(groups: List[dict], stats: List[str], mcqs: List[dict]) -> None:
    """Apply `repair()` one last time on every user-visible string.

    Even though parsing paths already call `repair()`, this final sweep catches
    any strings introduced later (e.g. via manual overrides or parser joins)
    and guarantees notes + topic-wise quiz stay in sync.
    """
    for g in groups:
        for it in g.get("items", []):
            txt = it.get("text")
            if isinstance(txt, str):
                it["text"] = repair(txt)
    for i, s in enumerate(stats):
        stats[i] = repair(s)
    for m in mcqs:
        if isinstance(m.get("q"), str):
            m["q"] = repair(m["q"])
        if isinstance(m.get("tag"), str):
            m["tag"] = repair(m["tag"])
        opts = m.get("opts", [])
        if isinstance(opts, list):
            m["opts"] = [repair(o) if isinstance(o, str) else o for o in opts]


