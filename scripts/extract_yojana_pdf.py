def is_noise(line: str) -> bool:
    s = line.strip()
    if not s:
        return True
    for rx in NOISE_PATTERNS:
        if rx.search(s):
            return True
    return False


# OCR fix-ups. The publisher's PDF uses a non-Unicode Marathi font that
# collapses several different Devanagari letters into the same OCR glyph,
# producing systematic substitutions across the whole document:
#   - bare "ि" stands in for several consonants (व, ह, त, घ)
#   - extra "ा" inserted before every "ं" (anusvara)
#   - "कक" / "हद" / "हड" / "सस" / "सश" / "जज" / "वव" appear at word starts
#     where the original had "कि" / "दि" / "डि" / "सि" / "शि" / "जि" / "वि"
#   - "र्" stands in for "ट" or "ग" depending on context
#   - "घ" is sometimes inserted as a phantom letter (e.g. अनुसूघचत)
#   - "य" + "ट" appears where the original had reph-ya cluster ("र्य")
# We address each with the safest pass we can — character-level rules
# only when the substitution is unambiguous (the OCR sequence is invalid
# Devanagari) and word-level overrides for everything else.
LONE_VOWEL_MARKS = "ािीुूेैोौंःृॅॉँ्"
LONE_VA_RX = re.compile(r"(?<=\s)ि(?=\s)")
TRAILING_LONE_MARK_RX = re.compile(r"\s+[" + re.escape(LONE_VOWEL_MARKS) + r"]\s*$")
EMBEDDED_LONE_MARK_RX = re.compile(r"\s+[" + re.escape(LONE_VOWEL_MARKS) + r"]\s+")
DEV_DIGIT_RX = re.compile(r"^[०-९]+$")
ZERO_WIDTH_RX = re.compile(r"[\u200B-\u200D\uFEFF]")
VIRAMA_BEFORE_DIGIT_RX = re.compile(r"्(?=[0-9०-९])")

# Marathi consonants set (used as guards in regex word-start fixes)
MR_CONSONANTS = "कखगघचछजझञटठडढतथदधनपफबभमयरलवशषसहळऱ"


def fix_stray_marks(text: str) -> str:
    """Repair OCR artifacts: bare "ि" -> "व" (publisher-font confusion),
    drop standalone trailing vowel marks ("... तयार करणारा े" -> "... तयार करणारा"),
    and tidy spacing around stripped marks."""
    if not text:
        return text
    # Drop hidden zero-width characters that leak from OCR/PDF extraction.
    text = ZERO_WIDTH_RX.sub("", text)
    # OCR occasionally leaves a dangling halant before digits ("₹्१०").
    text = VIRAMA_BEFORE_DIGIT_RX.sub("", text)
    text = LONE_VA_RX.sub("व", text)
    text = TRAILING_LONE_MARK_RX.sub("", text)
    text = EMBEDDED_LONE_MARK_RX.sub(" ", text)
    text = re.sub(r"[ \t]{2,}", " ", text)
    return text.strip()


# --------------------------------------------------------------------------- #
# Pass A: character-level systematic substitutions
# --------------------------------------------------------------------------- #
# These regexes target OCR sequences that are INVALID Devanagari, so the
# correction is mechanically safe.

# 1. Extra "ा" before "ं": collapse "ाां" (double ा + nasal) -> "ां" so the
#    legitimate plural-marker form is preserved on a second pass.
DOUBLE_AA_ANUSVARA_RX = re.compile(r"\u093E\u093E\u0902")

# 2. "कक" at word start (a doubled-KA without halant) -> "कि" (KA + I).
#    Marathi words don't start with "कक".
KK_START_RX = re.compile(r"(^|[^\u0900-\u097F])कक")

# 3. "हद" / "हड" / "हड" / "हट" / etc. at word start where the publisher's
#    font glyph for "ि" was misread as "ह" (which sits visually before
#    its consonant). Move the implicit "ि" after the next consonant.
HX_PREFIX_RX = re.compile(r"(^|[^\u0900-\u097F])ह([" + MR_CONSONANTS + "])")

# 4. "सस" / "सश" / "जज" / "वव" at word start: the OCR doubled the
#    leading consonant where the original had a "ि" vowel sign (which the
#    PDF font encodes as a separate prefix glyph that looked like the
#    consonant). Same fix as HX above but for these letter pairs.
DOUBLE_C_PREFIX_RX = re.compile(r"(^|[^\u0900-\u097F])(स|श|ज|व|प|भ|न|म|ल|र)\2")

# 5. "घ" inserted as a phantom letter inside common Marathi words —
#    only safe to remove when followed by a vowel sign that would
#    otherwise be invalid (eg "घच" / "घम" with following "ि" / "ी").
PHANTOM_GHA_RX = re.compile(r"घ(?=[चजसटठडमणनलि])")

# 6. "र्क्के" -> "टक्के" and "कोर्ी" -> "कोटी" (the "र्" glyph collides
#    with "ट" inside numbers/percent words).
RTI_NUM_RX = re.compile(r"र्(?=[ीे])")
RT_KKE_RX = re.compile(r"र्क्के")

# 7. "ांं" should never occur (double anusvara). Collapse.
DOUBLE_ANUSVARA_RX = re.compile(r"\u0902{2,}")


def char_pass(text: str) -> str:
    """Apply the safe character-level OCR substitutions described above."""
    if not text:
        return text
    # Step 1: collapse double-ा before nasal ("ाां" -> "ां"). This is the
    # only safe blanket rule for the OCR's extra-ा-before-nasal artifact.
    # We deliberately do NOT then collapse "ां" -> "ं", because legitimate
    # Marathi words (गांधी, पांढरी, सांधी, सांगणे, बांधकाम, ...) carry a
    # genuine "ा + nasal" that must not be touched. Specific words where
    # the OCR really did insert an extra ा are handled via WORD_FIXES /
    # TOKEN_FIXES / PHRASE_FIXES below (e.g. "अांगणवाडी" -> "अंगणवाडी").
    text = DOUBLE_AA_ANUSVARA_RX.sub("\u093E\u0902", text)
    # Step 3: word-start kk/hx/double-consonant fixes
    text = KK_START_RX.sub(r"\1कि", text)

    def _hx_repl(m: re.Match) -> str:
        return f"{m.group(1)}{m.group(2)}\u093F"
    text = HX_PREFIX_RX.sub(_hx_repl, text)

    def _dbl_c_repl(m: re.Match) -> str:
        return f"{m.group(1)}{m.group(2)}\u093F"
    text = DOUBLE_C_PREFIX_RX.sub(_dbl_c_repl, text)
    # Step 4: phantom "घ" before consonant cluster — but only if followed
    # by "ि" or "ी" (which is the actual OCR pattern, e.g. "अनुसूघचत").
    text = re.sub(r"घ(?=[चजसटठडमणनल](?:ि|ी))", "", text)
    # Step 5: percent/number र् confusion
    text = RT_KKE_RX.sub("टक्के", text)
    # Step 6: collapse spurious double anusvara
    text = DOUBLE_ANUSVARA_RX.sub("\u0902", text)
    # Step 7: tidy double spaces
    text = re.sub(r"[ \t]{2,}", " ", text)
    return text


